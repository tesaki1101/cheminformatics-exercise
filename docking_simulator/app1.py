import streamlit as st
import time

# ページ設定
st.set_page_config(
    page_title="化合物ドッキング・シミュレータ",
    page_icon="🧬",
    layout="centered"
)

# タイトルと説明文
st.title("🧬 化合物ドッキング・シミュレータ")
st.write("標的タンパク質（鍵穴）にぴったりハマる化合物（鍵）を設計しよう！")

st.markdown("""
高校の化学で習う**「官能基の性質」**を思い出して、右側の標的タンパク質（結合ポケット）の性質に合うように、化合物を改造してください。
""")

# 画面を2カラムに分ける
col1, col2 = st.columns(2)

with col1:
    st.subheader("🛠 化合物の改造（あなたの設計）")
    
    # 電荷の調整（プラスとマイナス）
    charge = st.radio(
        "1. 官能基の電荷（電気的な引き合い）",
        options=["プラス（アミノ基など）", "中性（メチル基など）", "マイナス（カルボキシ基など）"],
        index=1
    )
    
    # 親水性・疎水性の調整
    philic = st.slider(
        "2. 水へのなじみやすさ（親水性 ↔ 疎水性）",
        min_value=0,
        max_value=10,
        value=5,
        help="0: 油になじむ（疎水性/ベンゼン環など） / 10: 水になじむ（親水性/水酸基など）"
    )
    
    # 分子の大きさの調整
    size = st.slider(
        "3. 分子のサイズ（立体的なフィット感）",
        min_value=1,
        max_value=10,
        value=5,
        help="大きすぎるとポケットに入らず、小さすぎるとすき間ができてしまいます。"
    )

with col2:
    st.subheader("🎯 標的タンパク質（鍵穴の情報）")
    st.info("""
    **【ポケットAの環境】**
    * **電気的な性質:** マイナス（-）の電気を帯びたアミノ酸が露出しています。
    * **水へのなじみ:** 周囲は油になじみやすい（疎水性）アミノ酸で囲まれています。
    * **サイズ制限:** 中くらいの大きさ（サイズ：4〜6）の分子がすっぽり収まる穴です。
    """)

st.divider()

# 計算ボタン
if st.button("🚀 ドッキングシミュレーションを実行！", type="primary"):
    with st.spinner("AIと物理計算エンジンが結合エネルギーを計算中..."):
        time.sleep(1.5)  # 演出用のウェイト
        
    # スコア計算ロジック（高校化学の知識に基づく簡易判定）
    score = 0
    feedback = []
    
    # 1. 電荷の判定（ターゲットがマイナスなので、プラスが正解）
    if "プラス" in charge:
        score += 40
        feedback.append("⭕ **電荷:** 素晴らしい！マイナスの電気を帯びたポケットに対して、プラスのアミノ基が強力に静電結合（引き合い）しました！")
    elif "マイナス" in charge:
        score += 0
        feedback.append("❌ **電荷:** マイナス同士が反発してしまい、化合物が弾き飛ばされてしまいました。")
    else:
        score += 15
        feedback.append("🔺 **電荷:** 反発はしませんが、強い引き合いも生まれず、結合力が弱いです。")
        
    # 2. 親水性・疎水性の判定（ターゲットは疎水性なので、低い値(0〜3)が正解）
    if philic <= 3:
        score += 30
        feedback.append("⭕ **親水・疎水:** バッチリです！疎水性（油っぽい）のポケットと化合物のベンゼン環が綺麗に重なり、水分子を追い出して安定しました（疎水性相互作用）。")
    elif philic >= 7:
        score += 0
        feedback.append("❌ **親水・疎水:** 化合物が水になじみすぎて（親水性が高すぎて）、油っぽいポケットに入りたがりません。")
    else:
        score += 15
        feedback.append("🔺 **親水・疎水:** 悪くはないですが、もう少し疎水性を高めると（値を小さくすると）さらに密着します。")
        
    # 3. サイズの判定（ターゲットは4〜6がジャスト）
    if 4 <= size <= 6:
        score += 30
        feedback.append("⭕ **サイズ:** 完璧なフィット感です！鍵穴のサイズにぴったり収まりました。")
    elif size > 6:
        score -= 10
        score = max(0, score)
        feedback.append("❌ **サイズ:** 分子が大きすぎます！タンパク質の壁に衝突してしまい、ポケットの奥まで入りません（立体障害）。")
    else:
        score += 10
        feedback.append("🔺 **サイズ:** 小さすぎます！スカスカで隙間が空いているため、すぐに外れてしまいそうです。")

    # 結果表示
    st.header(f"📊 判定結果: {score} / 100 点")
    
    if score >= 90:
        st.balloons()
        st.success("🎉 特効薬の誕生です！完璧なドッキングを達成しました！高校化学の知識が見事に活きています！")
    elif score >= 50:
        st.warning("👍 惜しい！そこそこ効く薬にはなりそうですが、まだ改良の余地があります。親子でもう一度作戦を練り直しましょう！")
    else:
        st.error("😭 残念... 薬として機能しませんでした。タンパク質の『鍵穴の情報』をよく読んで、もう一度挑戦してください！")
        
    # 詳細フィードバック
    st.markdown("### 🧬 データサイエンス解説（親子で読もう）")
    for fb in feedback:
        st.markdown(fb)

st.sidebar.markdown("""
### 💡 高校生＆保護者へのメッセージ
このアプリでは簡易的に点数化していますが、実際の創薬データサイエンス（AI創薬）では、数百万個の化合物に対して**「電気の引き合い（クーロン力）」**や**「分子の形（立体障害）」**を数式化し、コンピュータで一瞬で計算（スクリーニング）しています。
みなさんが今やった「相性を考える思考」そのものが、最先端のAI創薬のアルゴリズムの基礎になっています！
""")
